% ------------------------------------------------------------------------------
% 
% compute 2D nbody problem
%
% ------------------------------------------------------------------------------
clear
close all
clc
% ..............................................................................
addpath('src/')
% ------------------------------------------------------------------------------
np = 3;
P = zeros(np,2,2);
M = 2*ones(np,1);

P(1,:,1) = [0 0];
P(2,:,1) = [0.5 3];
P(3,:,1) = [2 4];

r_=P(:,:,1);
% ------------------------------------------------------------------------------
figure;
plot(r_(:,1),r_(:,2),'k.','markersize',50)
simple_figure()
% ------------------------------------------------------------------------------
dt = 0.05;
nt = 100;
[P,M] = nbody(P,M,dt,nt);
r = P(:,:,1);
% ------------------------------------------------------------------------------
figure;
hold on
plot(r(:,1),r(:,2),'r.','markersize',50)
plot(r_(:,1),r_(:,2),'k.','markersize',40)
hold off
simple_figure()
