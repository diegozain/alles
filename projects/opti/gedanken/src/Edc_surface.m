function EE = Edc_surface(p1,p2,s_dc,Mdc,d_dc_o,Ndc)
% ..............................................................................
np1 = length(p1);
np2 = length(p2);
EE = zeros(np1,np2);
% ..............................................................................
for i=1:np1
  for j=1:np2
    % ..........................................................................
    % point in parameter space
    %
    p = [p1(i) ; p2(j)];
    % ..........................................................................
    % forward model dc
    %
    [u_dc,e_dc] = fwd_dc(p,s_dc,Mdc,d_dc_o);
   % ..........................................................................
    % store in matrix
    %
    EE(i,j) = Edc_obj(e_dc,Ndc);
  end
end
% ..............................................................................
end
