program qrfits_ie
 ! ----------------------------------------------------------------------
 ! 🎛️🖥️
 !
 !                           🟪🔴 = 🔷🔺
 !
 !
 ! Ax = b
 ! AP = QR
 ! -----------------------------------------------------------------------
 use qrfits
 implicit none
 ! -----------------------------------------------------------------------
 integer, parameter :: nd=5
 double precision :: x(nd), y(nd)

 double precision :: bl(2), bp(3)
 integer :: ii

 ! --------------------------------------------------------------------------
 x(1) = 0
 x(2) = 1
 x(3) = 3
 x(4) = 4
 x(5) = 5

 y(1) = 5
 y(2) = 2
 y(3) = 10
 y(4) = 20
 y(5) = 50
 ! --------------------------------------------------------------------------
 !
 !
 !                    🎸 lets rock and roll 💃
 !
 !
 ! ---------------------------------------------------------------------------
 call linefit(nd,x,y,bl)
 call parafit(nd,x,y,bp)
 ! ⭐🌟🌠
 ! remember that
 !               xmin = - bp(2) / (2*bp(1))
 ! ---------------------------------------------------------------------------
 print *, ''
 print *, ' the line ax + b is given by'
 do ii=1,2
   write(*,*) ' ', bl(ii)
 enddo
 print *, ''
 print *, ' the parabola ax² + bx + c is given by'
 do ii=1,3
   write(*,*) ' ', bp(ii)
 enddo
 print *, ''
 ! ---------------------------------------------------------------------------
end program qrfits_ie